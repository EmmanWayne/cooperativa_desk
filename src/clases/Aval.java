package clases;

public class Aval {
	private String id_aval;
	private String identidad_aval;
	private String nombre_aval;
	private String telefono;
	private String direccion;
	private boolean afiliado;
	private boolean trabaja;
	private String lugar_trabajo;
	private String afinidad;
	private String ingreso_mensual;
	private String identidad;
	
	public String getId_aval() {
		return id_aval;
	}
	public void setId_aval(String id_aval) {
		this.id_aval = id_aval;
	}
	public String getIdentidad_aval() {
		return identidad_aval;
	}
	public void setIdentidad_aval(String identidad_aval) {
		this.identidad_aval = identidad_aval;
	}
	public String getNombre_aval() {
		return nombre_aval;
	}
	public void setNombre_aval(String nombre_aval) {
		this.nombre_aval = nombre_aval;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public boolean getAfiliado() {
		return afiliado;
	}
	public void setAfiliado(boolean afiliado) {
		this.afiliado = afiliado;
	}
	public boolean getTrabaja() {
		return trabaja;
	}
	public void setTrabaja(boolean trabaja) {
		this.trabaja = trabaja;
	}
	public String getLugar_trabajo() {
		return lugar_trabajo;
	}
	public void setLugar_trabajo(String lugar_trabajo) {
		this.lugar_trabajo = lugar_trabajo;
	}
	public String getAfinidad() {
		return afinidad;
	}
	public void setAfinidad(String afinidad) {
		this.afinidad = afinidad;
	}
	public String getIngreso_mensual() {
		return ingreso_mensual;
	}
	public void setIngreso_mensual(String ingreso_mensual) {
		this.ingreso_mensual = ingreso_mensual;
	}
	public String getIdentidad() {
		return identidad;
	}
	public void setIdentidad(String identidad) {
		this.identidad = identidad;
	}
}
