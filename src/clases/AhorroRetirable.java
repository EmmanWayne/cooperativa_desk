package clases;

public class AhorroRetirable {
	private String id_retirable;
	private String Identidad;
	private String Cantidad_ahorrar;
	private String Fecha;
	
	public String getId_retirable() {
		return id_retirable;
	}
	public void setId_retirable(String id_retirable) {
		this.id_retirable = id_retirable;
	}
	public String getIdentidad() {
		return Identidad;
	}
	public void setIdentidad(String identidad) {
		Identidad = identidad;
	}
	public String getCantidad_ahorrar() {
		return Cantidad_ahorrar;
	}
	public void setCantidad_ahorrar(String cantidad_ahorrar) {
		Cantidad_ahorrar = cantidad_ahorrar;
	}
	public String getFecha() {
		return Fecha;
	}
	public void setFecha(String fecha) {
		Fecha = fecha;
	}
}
