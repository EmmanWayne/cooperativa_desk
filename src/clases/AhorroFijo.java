package clases;

public class AhorroFijo {
	private String id_ahorro_fijo;
	private String Identidad;
	private String Cantidad_fija_ahorrar;
	private String Fecha_deposita_fijo;
	
	public String getId_ahorro_fijo() {
		return id_ahorro_fijo;
	}
	public void setId_ahorro_fijo(String id_ahorro_fijo) {
		this.id_ahorro_fijo = id_ahorro_fijo;
	}
	public String getIdentidad() {
		return Identidad;
	}
	public void setIdentidad(String identidad) {
		Identidad = identidad;
	}
	public String getCantidad_fija_ahorrar() {
		return Cantidad_fija_ahorrar;
	}
	public void setCantidad_fija_ahorrar(String cantidad_fija_ahorrar) {
		Cantidad_fija_ahorrar = cantidad_fija_ahorrar;
	}
	public String getFecha_deposita_fijo() {
		return Fecha_deposita_fijo;
	}
	public void setFecha_deposita_fijo(String fecha_deposita_fijo) {
		Fecha_deposita_fijo = fecha_deposita_fijo;
	}
}
